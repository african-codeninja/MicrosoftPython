first_name = input('Please enter your First name: ')
last_name = input('Please enter your Last name: ')

print('This is just outputting a string')
print(first_name + ' '+ last_name)
print('')
print('This is just outputting a string but capitalizing the fist letter using the .capitalize() method')
print('Hello,' + first_name.capitalize() + ' '+ last_name.capitalize())
print('')
print('This is just outputting a string capitalizing all the letters using .upper() method')
print('Hello,' + first_name.upper() + ' '+ last_name.upper())